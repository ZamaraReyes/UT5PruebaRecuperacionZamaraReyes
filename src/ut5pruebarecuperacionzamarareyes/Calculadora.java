package ut5pruebarecuperacionzamarareyes;

/**
 *
 * @author ZareRed
 */
public class Calculadora {
    
    private String Suma;
    private String Resta;
    private String Multiplicar;
    private String Dividir;

    /**
     * @return the Suma
     */
    public String getSuma() {
        return Suma;
    }

    /**
     * @param Suma the Suma to set
     */
    public void setSuma(String Suma) {
        this.Suma = Suma;
    }

    /**
     * @return the Resta
     */
    public String getResta() {
        return Resta;
    }

    /**
     * @param Resta the Resta to set
     */
    public void setResta(String Resta) {
        this.Resta = Resta;
    }

    /**
     * @return the Multiplicar
     */
    public String getMultiplicar() {
        return Multiplicar;
    }

    /**
     * @param Multiplicar the Multiplicar to set
     */
    public void setMultiplicar(String Multiplicar) {
        this.Multiplicar = Multiplicar;
    }

    /**
     * @return the Dividir
     */
    public String getDividir() {
        return Dividir;
    }

    /**
     * @param Dividir the Dividir to set
     */
    public void setDividir(String Dividir) {
        this.Dividir = Dividir;
    }
    
}
